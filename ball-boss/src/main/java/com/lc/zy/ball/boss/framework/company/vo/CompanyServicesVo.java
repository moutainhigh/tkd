package com.lc.zy.ball.boss.framework.company.vo;

import com.lc.zy.ball.domain.oa.po.CompanyServices;

public class CompanyServicesVo extends CompanyServices {
	private static final long serialVersionUID = -8089669376156352937L;
	private String name;
	private String serviceType_;
	private Integer totalMoney;

	public Integer getTotalMoney() {
		return totalMoney;
	}

	public void setTotalMoney(Integer totalMoney) {
		this.totalMoney = totalMoney;
	}

	/**
	 * 类型：收款、退款
	 */
	private String type;
	
	

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getServiceType_() {
		return serviceType_;
	}

	public void setServiceType_(String serviceType_) {
		this.serviceType_ = serviceType_;
	}
	
}
