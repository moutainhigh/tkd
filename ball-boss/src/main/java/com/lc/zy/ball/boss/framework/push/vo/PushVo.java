package com.lc.zy.ball.boss.framework.push.vo;

import java.util.Date;
import java.util.List;

import com.lc.zy.ball.domain.oa.po.Push;

public class PushVo extends Push{
	
	private List<Push> childPush;
	
	private String pushRange;
	
	private Date pushDate;
	
	private String pushCt;
	
	private String logId;
	
	private String pushTitle;
	
	private String isPush;
	
	private String pState;
	
	private String factPushTime;
	
	public List<Push> getChildPush() {
		return childPush;
	}

	public void setChildPush(List<Push> childPush) {
		this.childPush = childPush;
	}

	public String getPushRange() {
		return pushRange;
	}

	public void setPushRange(String pushRange) {
		this.pushRange = pushRange;
	}

	public Date getPushDate() {
		return pushDate;
	}

	public void setPushDate(Date pushDate) {
		this.pushDate = pushDate;
	}

	public String getPushCt() {
		return pushCt;
	}

	public void setPushCt(String pushCt) {
		this.pushCt = pushCt;
	}

	public String getLogId() {
		return logId;
	}

	public void setLogId(String logId) {
		this.logId = logId;
	}

	public String getPushTitle() {
		return pushTitle;
	}

	public void setPushTitle(String pushTitle) {
		this.pushTitle = pushTitle;
	}

	public String getIsPush() {
		return isPush;
	}

	public void setIsPush(String isPush) {
		this.isPush = isPush;
	}

	public String getpState() {
		return pState;
	}

	public void setpState(String pState) {
		this.pState = pState;
	}

	public String getFactPushTime() {
		return factPushTime;
	}

	public void setFactPushTime(String factPushTime) {
		this.factPushTime = factPushTime;
	}

}
