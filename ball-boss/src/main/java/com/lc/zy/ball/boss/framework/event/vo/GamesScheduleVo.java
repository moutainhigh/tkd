package com.lc.zy.ball.boss.framework.event.vo;

import java.util.List;

import com.lc.zy.ball.domain.oa.po.CorpsInfo;

@SuppressWarnings("unused")
public class GamesScheduleVo {
	private int turnNo;

	private List<GroupInfo> groupList;

	private GroupInfo groupInfo;

	public int getTurnNo() {
		return turnNo;
	}

	public void setTurnNo(int turnNo) {
		this.turnNo = turnNo;
	}

	public List<GroupInfo> getGroupList() {
		return groupList;
	}

	public void setGroupList(List<GroupInfo> groupList) {
		this.groupList = groupList;
	}

	public GroupInfo getGroupInfo() {
		return groupInfo;
	}

	public void setGroupInfo(GroupInfo groupInfo) {
		this.groupInfo = groupInfo;
	}

	public static class GroupInfo {
		private String groupName;
		private String type;
		private String gameTime;
		private List<CorpsInfoVo> corps;

		public String getGameTime() {
			return gameTime;
		}

		public void setGameTime(String gameTime) {
			this.gameTime = gameTime;
		}

		public String getGroupName() {
			return groupName;
		}

		public void setGroupName(String groupName) {
			this.groupName = groupName;
		}

		public String getType() {
			return type;
		}

		public void setType(String type) {
			this.type = type;
		}

		public List<CorpsInfoVo> getCorps() {
			return corps;
		}

		public void setCorps(List<CorpsInfoVo> corps) {
			this.corps = corps;
		}
	}

}
