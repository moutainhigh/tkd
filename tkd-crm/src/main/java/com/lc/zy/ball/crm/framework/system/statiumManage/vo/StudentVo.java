package com.lc.zy.ball.crm.framework.system.statiumManage.vo;

import com.lc.zy.ball.common.data.pageable.Page;
import com.lc.zy.ball.domain.oa.po.Order;
import com.lc.zy.ball.domain.oa.po.SsoUser;

/**
 * Created by sl on 2016/12/15.
 */
public class StudentVo extends SsoUser {
    // 期限卡期限
    private String dateTime;
    // 账户余额
    private int balance;
    // 订单数
    private int orderNum;
    // 消费总金额
    private int amount;
    // 订单page
    Page<Order> pageData;

    public int getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(int orderNum) {
        this.orderNum = orderNum;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public Page<Order> getPageData() {
        return pageData;
    }

    public void setPageData(Page<Order> pageData) {
        this.pageData = pageData;
    }

    public String getDateTime() {
        return dateTime;
    }

    public void setDateTime(String dateTime) {
        this.dateTime = dateTime;
    }

    public int getBalance() {
        return balance;
    }

    public void setBalance(int balance) {
        this.balance = balance;
    }
}
